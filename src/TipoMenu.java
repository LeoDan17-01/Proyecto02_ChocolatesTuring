/**
 * Enum que representa los tipos de menu disponibles
 * Usado para clasificar los menus segun el tiempo del dia
 */
public enum TipoMenu {
    /**
     * Menu correspondiente al desayuno
     */
    DESAYUNO,

    /**
     * Menu correspondiente a la comida
     */
    COMIDA,

    /**
     * Menu correspondiente a la cena
     */
    CENA
}
