/**
 * Enum que representa los diferentes tipos de usuario
 * Define el rol del usuario dentro del sistema del comedor
 */
public enum TipoUsuario {
    /**
     * Usuario con rol de estudiante
     */
    ESTUDIANTE,

    /**
     * Usuario con rol de profesor
     */
    PROFESOR,

    /**
     * Usuario con rol de visitante
     */
    VISITANTE,

    /**
     * Usuario que trabaja en el area de ciencias
     */
    TRABAJADOR_CIENCIAS
}
